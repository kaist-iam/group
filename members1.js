function openModal(memberId) {
  const member = membersData[memberId];
  if (!member) return;
 
  let linksHtml = "";
  if (member.links && (member.links.website || member.links.scholar || member.links.linkedin)) {
    linksHtml = '<div class="modal-links">';
    if (member.links.website) {
      linksHtml += `<a href="${member.links.website}" target="_blank" class="modal-link"><i class="fas fa-globe"></i> Website</a>`;
    }
    if (member.links.scholar) {
      linksHtml += `<a href="${member.links.scholar}" target="_blank" class="modal-link"><i class="ai ai-google-scholar"></i> Google Scholar</a>`;
    }
    if (member.links.linkedin) {
      linksHtml += `<a href="${member.links.linkedin}" target="_blank" class="modal-link"><i class="fab fa-linkedin"></i> LinkedIn</a>`;
    }
    linksHtml += "</div>";
  }
 
  let publicationsHtml = "";
  if (member.publications && member.publications.length > 0) {
    publicationsHtml = `
      <div class="modal-section">
        <h3>Selected Publications</h3>
        <ul class="modal-edu">
          ${member.publications.map(pub => `<li>${pub}</li>`).join("")}
        </ul>
      </div>
    `;
  }
 
const html = `
    <div class="modal-header" style="display:flex; flex-direction:row; align-items:center; gap:1.5rem; padding:1rem 1.5rem 1.15rem; justify-content:flex-start; background:linear-gradient(180deg,#ffffff 0%,#fbfdff 100%); border-bottom:1px solid #e5e7eb; border-radius:22px 22px 0 0;">
      <img src="${member.photo}" alt="${member.name}" style="width:158px; aspect-ratio:3/4; border-radius:18px; object-fit:cover; border:3px solid #eef3f8; box-shadow:0 8px 18px rgba(15,23,42,0.08); flex-shrink:0;">
      <div style="display:flex; flex-direction:column; gap:0.25rem;">
        <h2 style="font-size:1.4rem; font-weight:780; margin:0; color:#111827; letter-spacing:-0.02em;">${member.name}</h2>
        <p style="font-size:0.96rem; color:#6b7280; margin:0; font-weight:500;">${member.role}</p>
        ${linksHtml}
      </div>
    </div>
 
    <div class="modal-section">
      <h3>Education & Experience</h3>
      <ul class="modal-edu">
        ${member.education.map(item => `<li>${item}</li>`).join("")}
      </ul>
    </div>
 
    <div class="modal-section">
      <h3>Research Interests</h3>
      <div class="modal-tags">
        ${member.tags.map(tag => `<span>${tag}</span>`).join("")}
      </div>
    </div>
 
    ${publicationsHtml}
 
    <div class="modal-section">
      <h3>About</h3>
      <p class="modal-bio">${member.bio}</p>
    </div>
  `;
 
  document.getElementById("modalBody").innerHTML = html;
  document.getElementById("memberModal").style.display = "flex";
  document.body.style.overflow = "hidden";
}
 
function closeModal(event) {
  if (!event || event.target.id === "memberModal" || event.target.className === "modal-close") {
    document.getElementById("memberModal").style.display = "none";
    document.body.style.overflow = "auto";
  }
}
 
document.addEventListener("keydown", function (e) {
  if (e.key === "Escape") closeModal();
});
